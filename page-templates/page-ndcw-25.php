                    <div class="card ncw-card">
                        <h2 id="what-is-neurodiversity" class="text-center text-white">What is Neurodiversity?</h2>
                        <div class="card-body text-white">
                            <p class="card-text">When it comes to inclusion, neurodiversity refers to a world where neurological differences are recognised and respected as all other human variations.</p>

                            <p class="card-text">Depending on how our brains are wired we think, move, process information and communicate in different ways. Many people in our community use neurodiversity as an umbrella term used to describe alternative thinking styles such as Dyslexia, DCD (Dyspraxia), Dyscalculia, Autism and ADHD. But regardless of labels, neurodiversity is about recognising those who think differently. And no matter whether you are a school, university or organisation you can benefit from different thinkers!</p>
 
                            <p class="card-text">Approximately 15-20% of population has a neurological difference.Instead of labelling people with deficits or disorders, when we use the term neurodiversity, we take a balanced view of an individual’s unique strengths and challenges. Many ‘challenges’ neurodivergent people face are more to do with the environment and systems they are placed in, often designed by a majority population.</p>
